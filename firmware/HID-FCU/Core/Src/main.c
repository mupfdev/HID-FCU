/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 5 */
  bool fast_bootup = false;

  init_usb_hid_handler();
  init_switch_panel();
  init_autopilot();
  init_levers();

  /* Infinite loop */
  while(1)
  {
    uint32_t battery_state;
    uint32_t av_bus2_state;
    uint32_t switch_state;

    switch (get_state())
    {
      case STATE_OFF:
      {
        switch_state  = get_switch_state();
        battery_state = (switch_state & (1U << SW_BATTERY));
        av_bus2_state = (switch_state & (1U << SW_AVIONIC_BUS2));

        if (0 == (switch_state & (1U << SW_SYNC_MODE)))
        {
          fast_bootup = true;
        }
        else
        {
          fast_bootup = false;
        }

        if (0 == battery_state && 0 == av_bus2_state)
        {
          set_state(STATE_AVIONICS_BUS2_BOOTUP);
        }
        break;
      }
      case STATE_AVIONICS_BUS2_BOOTUP:
      {
        for (int c = 1; c <= 4; c+= 1)
        {
          set_display(SEG_PFT, c);

          switch_state  = get_switch_state();
          battery_state = (switch_state & (1U << SW_BATTERY));
          av_bus2_state = (switch_state & (1U << SW_AVIONIC_BUS2));

          if (battery_state > 0 || av_bus2_state > 0)
          {
            set_state(STATE_OFF);
            clear_display();
            break;
          }

          if (false == fast_bootup)
          {
            osDelay(1800);
          }
          else
          {
            osDelay(0);
          }
        }

        switch_state  = get_switch_state();
        battery_state = (switch_state & (1U << SW_BATTERY));
        av_bus2_state = (switch_state & (1U << SW_AVIONIC_BUS2));

        if (battery_state > 0 || av_bus2_state > 0)
        {
          set_state(STATE_OFF);
          clear_display();
          break;
        }

        if (false == fast_bootup)
        {
          osDelay(2000);
        }
        else
        {
          osDelay(0);
        }

        fill_display();

        switch_state  = get_switch_state();
        battery_state = (switch_state & (1U << SW_BATTERY));
        av_bus2_state = (switch_state & (1U << SW_AVIONIC_BUS2));

        if (battery_state > 0 || av_bus2_state > 0)
        {
          set_state(STATE_OFF);
          clear_display();
          break;
        }

        if (false == fast_bootup)
        {
          osDelay(4000);
        }
        else
        {
          osDelay(0);
        }

        disable_autopilot();
        set_state(STATE_READY);

        clear_display();
        set_display(SEG_OFF, 0);

        break;
      }
      case STATE_READY:
      {
        switch_state  = get_switch_state();
        battery_state = (switch_state & (1U << SW_BATTERY));
        av_bus2_state = (switch_state & (1U << SW_AVIONIC_BUS2));

        if (battery_state > 0 || av_bus2_state > 0)
        {
          set_state(STATE_OFF);
          clear_display();
        }
        break;
      }
    }

    osDelay(1);
  }
  /* USER CODE END 5 */
}

